package com.food.bean;

public class users {
    int sapid;
    String password;
    String detail;

    public void setSapid(int sapid) {
        this.sapid = sapid;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public int getSapid() {
        return sapid;
    }

    public String getPassword() {
        return password;
    }

    public String getDetail() {
        return detail;
    }
}
